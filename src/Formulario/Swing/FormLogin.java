    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String[] Usuario = {"diana","jorge","roger"};
        String[] Contraseña = {"123","456","789"};
        boolean verificado = false;
        int intentos=0;
        
        String usuario = txtUsuario.getText();
        String contraseña = String.valueOf(txtContraseña.getPassword());
        
        for (int i=0; i<Usuario.length; i++)
        {
            if (Usuario[i].equals(usuario)&&Contraseña[i].equals(contraseña))
            {
                verificado=true;
                JOptionPane.showMessageDialog(null,"Acceso Correcto\nBienvenido!");
            }
        }
        if (verificado)
        {
            Principal formulario = new Principal();
            formulario.setVisible(true);
            this.dispose();
        } 
        else
        {
            intentos++;
            if (intentos<3)
            {
                JOptionPane.showMessageDialog(null,"Usuario o Contraseña invalida!\nIntente de nuevo.");
                txtUsuario.setText("");
                txtContraseña.setText("");
                txtUsuario.requestFocus();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Excediste el número de intentos!");
                System.exit(0);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

